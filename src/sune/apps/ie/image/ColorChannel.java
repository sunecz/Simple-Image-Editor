package sune.apps.ie.image;

/**
 * Stores all supported color channels.*/
public enum ColorChannel
{
	/**
	 * Represents the red color
	 * channel*/
	RED,
	/**
	 * Represents the green color
	 * channel*/
	GREEN,
	/**
	 * Represents the blue color
	 * channel*/
	BLUE,
	/**
	 * Represents the alpha 
	 * (transparency) channel*/
	ALPHA,
	/**
	 * Represents the red, green,
	 * blue and alpha channel*/
	RGBA,
	/**
	 * Represents the red, green
	 * and blue channel*/
	RGB;
}